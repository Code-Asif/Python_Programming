#Python Program to demonstrate the working of built-in numeric functions ceil(), floor(), fabs(), factorial(), gcd() by importing math module.

import math

# prints the ceil of 4.4
print("The ceil of 4.4 is : ", end="")
print(math.ceil(4.4))

# prints the floor of 4.4
print("The floor of 4.4 is : ", end="")
print(math.floor(4.4))

# prints the fabs of -4.4
print("The fabs of -4.4 is : ", end="")
print(math.fabs(-4.4))

# prints the factorial of 4
print("The factorial of 4 is : ", end="")
print(math.factorial(4))

# prints the gcd of 24 and 18
print("The gcd of 24 and 18 is : ", end="")
print(math.gcd(24, 18))